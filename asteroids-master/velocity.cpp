
#include "velocity.h"
#include <stdio.h>

/******************************************************************
 * VELOCITY - CONTRUCTOR
 ******************************************************************/
Velocity :: Velocity()
{
    dx = 0;
    dy = 0;
}

/******************************************************************
 * VELOCITY - CONSTRUCTOR
 ******************************************************************/
Velocity :: Velocity(float dx, float dy)
{
    this->dx = dx;
    this->dy = dy;
}

/******************************************************************
 * GET DX
 ******************************************************************/
float Velocity :: getDx() const
{
    return dx;
}

/******************************************************************
 * GET DY
 ******************************************************************/
float Velocity :: getDy() const
{
    return dy;
}

/******************************************************************
 * SET DX
 ******************************************************************/
void Velocity :: setDx(float dx)
{
    this->dx = dx;
}

/******************************************************************
 * SET DY
 ******************************************************************/
void Velocity :: setDy(float dy)
{
    this->dy = dy;
}
